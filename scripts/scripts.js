function createLi(clase = "") {
    const list = document.createElement("li");
    list.className = clase;
    return list;
}
function createP(cont) {
    const p = document.createElement("p");
    p.innerText = cont;
    return p;

}
function createDiv(clase) {
    const div = document.createElement("div");
    div.className = clase;
    return div;
}
function createHr() {
    const hr = document.createElement("hr");
    return hr;
}
function createOl() {
    const ol = document.createElement("ol");
    return ol;
}
function createH3(value) {
    const h3 = document.createElement("h3")
    h3.innerText = value
    return h3;
}
function createImg(imagen, alt, clase = "") {

    const div = document.createElement("div");
    const img = document.createElement("img");

    // Añadir clases y atributos
    div.className = clase;
    img.src = "https://lh3.googleusercontent.com/d/" + imagen;
    img.alt = alt;

    div.appendChild(img);

    return div;
}
function createImg2(imagen, alt) {

    const img = document.createElement("img");
    // Añadir clases y atributos
    img.src = "https://lh3.googleusercontent.com/d/" + imagen;
    img.alt = alt;
    return img;
}
function createName(name, href) {
    const h4 = document.createElement("h4");
    const a = document.createElement("a");

    h4.className = "productos-nombre";
    h4.id = "data-nombre";
    a.href = href;
    a.className = "link-comprar";
    a.innerHTML = name;

    h4.appendChild(a);

    return h4;
}
function createPrice(precio, oferta, desc) {

    const div = document.createElement("div");
    const h5 = document.createElement("h5");
    const h6 = document.createElement("h6");
    const p = document.createElement("p");


    if (desc > 25) {
        div.className = "productos-descuento hot";
        h6.innerHTML = "HOT " + desc + "%"
    }
    else if (desc != 0) {
        div.className = "productos-descuento";
        h6.innerHTML = desc + "% OFF";
    }

    h6.className = "data-off";
    h5.className = "data-oferta";
    if (oferta != 0) {
        h5.innerHTML = "$" + oferta;
    }
    p.className = "data-precio";
    p.innerHTML = "$" + precio;

    div.appendChild(h5);
    div.appendChild(h6);
    div.appendChild(p);

    return div;
}
function createBtn(identif, href) {

    const a = document.createElement("a");
    const btn = document.createElement("button");

    a.href = href;
    a.className = "link-comprar";
    btn.className = "productos-boton";
    btn.textContent = "Comprar";
    btn.id = identif;

    a.appendChild(btn);
    return a;
}
function createCuotas(oferta, original) {
    const h5 = document.createElement("h5")
    h5.className = "productos-cuotas";

    for (let i = 0; i < original.length; i++) {
        oferta = oferta.replace(".", '')
        original = original.replace(".", '')
    }
    const conDesc = parseInt(oferta)
    const sinDesc = parseInt(original)
    if (conDesc != 0) {
        h5.textContent = "Mismo precio en 12 cuotas de $" + conDesc / 10;
    }
    else {
        h5.textContent = "mismo precio en 12 cuotas de $" + sinDesc / 10;
    }

    return h5;
}

// funcion para crear los ol>li
function producto(prod, clase, links, showProductStart = 0, cant = 3) {
    const ol = document.querySelectorAll(clase);
    const fragment = document.createDocumentFragment(); // Crear un fragmento de documento para agregar los elementos
    const li = [];
    const img = [];
    const h4 = [];
    const precio = [];
    const cuotas = [];
    const boton = [];
    console.log(prod.length, showProductStart + cant)
    if (prod.length - showProductStart < cant) {
        cant = prod.length - showProductStart;
    }

    for (let j = 0; j < ol.length; j++) {
        for (let i = 0, k = showProductStart; i < cant; i++, k++) {
            img[i] = createImg(prod[k].img[0], prod[k].alt, "productos-img");
            h4[i] = createName(prod[k].nombre, links);
            precio[i] = createPrice(prod[k].precio[0], prod[k].precio[1], prod[k].precio[2]);
            cuotas[i] = createCuotas(prod[k].precio[1], prod[k].precio[0]);
            boton[i] = createBtn(prod[k].id, links);
            li[i] = createLi("productos-card");
            li[i].appendChild(img[i]);
            li[i].appendChild(h4[i]);
            li[i].appendChild(precio[i]);
            li[i].appendChild(cuotas[i]);
            li[i].appendChild(boton[i]);
            fragment.appendChild(li[i]);
        }
        ol[j].appendChild(fragment);
    }

    return ol;
}

